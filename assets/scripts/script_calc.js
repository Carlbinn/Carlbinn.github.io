function mySum(par1, par2){
    return par1 + par2
}

function myDif(par1, par2) {
    return par1 - par2
}

function myMult(par1, par2){
    return par1 * par2
}

function myDiv(par1, par2){
    return par1 / par2
}

x = 3
y = 1

console.log(mySum(x,y))
console.log(myDif(x,y))
console.log(myMult(x,y))
console.log(myDiv(x,y))